sushi = %w[salmon tuna shrimp eel yellowtail]

# shortcut for sushi[0] as a string
p sushi.first

# pulls out an array from first till 4th element as an array
p sushi.first 4

# shortcut for sushi[-1] as a string
p sushi.last

# pulls out an array from first till 4th element as an array
# saves order
p sushi.last 2